package com.troymorrow.Day1;

public class Demo5 {
    public static void main(String[] args) {

        int number = 4;
        switch (number) {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("Not a valid number");
        }
    }
}
